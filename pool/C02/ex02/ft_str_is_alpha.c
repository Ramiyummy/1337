int	ft_str_is_alpha(char *str)
{
	char	c;
	int		alpha;

	alpha = 1;
	while (str != 0 && *str != '\0')
	{
		c = *str;
		if (!((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z')))
		{
			alpha = 0;
			break ;
		}
		++str;
	}
	return (alpha);
}
